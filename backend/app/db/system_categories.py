def make_translation_key(*parts: str) -> str:
    normalized = "_".join(parts).lower()
    replacements = {
        " ": "_",
        "/": "_",
        "-": "_",
        "ё": "е",
    }
    for source, target in replacements.items():
        normalized = normalized.replace(source, target)
    return f"categories.{normalized}"

